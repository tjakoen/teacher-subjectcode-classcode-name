// Shared AI feedback engine for the grade sweep.
//
// IDENTICAL across every class - the only per-class differences live in
// grader/<id>/ (the tests, the RUBRIC.md) and grader/class-prompt.md. Keeping
// this in one file is deliberate: the grading architecture must not drift
// between classes, only the tech-stack-specific tests do.
//
// Generated AFTER grading, only for assignments that opt in
// (`"ai-grading": true`) and only for rows actually (re)graded this run. With no
// MODELS_TOKEN the whole feature no-ops, so grading never depends on it.
//
// Provider: GitHub Models (OpenAI-compatible), reached with the Actions
// built-in token (which carries `models: read`). Swapping providers is a change
// to this one file. Output is split into a student-facing half (no scores, no
// AI attribution) and an instructor-facing half (adds a proposed rubric score
// and triage flags) so the same call serves both audiences.

import { execSync } from "node:child_process";
import { readFileSync, readdirSync } from "node:fs";

const MODELS_TOKEN = process.env.MODELS_TOKEN || "";
const MODELS_ENDPOINT = process.env.MODELS_ENDPOINT || "https://models.github.ai/inference/chat/completions";
const MODELS_NAME = process.env.MODELS_MODEL || "openai/gpt-4o-mini";
export const aiEnabled = () => !!MODELS_TOKEN;

const sh = (cmd) => execSync(cmd, { encoding: "utf8", stdio: ["ignore", "pipe", "pipe"] }).trim();
const quiet = (cmd) => execSync(cmd, { stdio: "ignore" });

// The free / personal Models tier is rate limited per minute, so keep
// concurrency low and back off on 429 instead of giving up. Tunable per class.
const POOL = Math.max(1, +process.env.FEEDBACK_CONCURRENCY || 2);
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

// One model call with 429 backoff. Honors Retry-After (capped), retries a few
// times with jitter, then returns the last response (caller treats !ok as skip).
async function callModel(body) {
  for (let attempt = 0; ; attempt++) {
    let res;
    try {
      res = await fetch(MODELS_ENDPOINT, {
        method: "POST",
        headers: { "Content-Type": "application/json", Authorization: `Bearer ${MODELS_TOKEN}` },
        body: JSON.stringify(body),
      });
    } catch (e) {
      if (attempt < 2) { await sleep(2000 * (attempt + 1)); continue; } // transient network
      throw e;
    }
    if (res.status === 429 && attempt < 3) {
      const ra = +res.headers.get("retry-after");
      const waitMs = Math.min((Number.isFinite(ra) && ra > 0 ? ra : 15) * 1000, 60000) + Math.floor(Math.random() * 3000);
      await sleep(waitMs);
      continue;
    }
    return res;
  }
}

// Constant persona + hard guardrails. Kept in code (not the class prompt) so a
// class cannot edit the guardrails away.
const SYSTEM_PROMPT = [
  "You are a teaching assistant for a course. Write short, formative feedback a student can act on, in plain language at their level.",
  "Comment on code quality across the logic and any frontend: structure, naming, error handling, and clarity.",
  "When a screenshot is provided AND the page is genuinely styled, also comment on visual design (layout, hierarchy, spacing, consistency, accessibility), grounded in the rubric when one is given.",
  "When asked, you also privately estimate for the instructor how likely the work was generated by an AI assistant rather than written by a student at the stated level - a soft signal for their judgement, never an accusation, and NEVER shown to the student.",
  "Hard rules: NEVER give corrected code or the exact fix; instead name the concept to revisit or ask a guiding question. Be encouraging and concise.",
].join(" ");

// Per-class goals / tech stack / level, authored in grader/class-prompt.md.
const classPrompt = () => {
  try { return readFileSync("grader/class-prompt.md", "utf8").trim(); } catch { return ""; }
};

// A row warrants feedback when its assignment opted in AND it is either a
// project (any score) or an activity that did not score perfectly. Locked
// (frozen) activities and unbuildable submissions (no tests ran) never spend
// tokens. The commit-hash skip in the sweep is the OUTER idempotency gate (a
// submission is only (re)graded when its sha changes); this is the inner
// content gate.
export function warrantsFeedback(row, a) {
  if (!a || !a["ai-grading"]) return false;
  if (a.locked) return false;        // frozen activity: never generate feedback
  if (!row.total) return false;      // nothing graded (malformed / wrong template): no tokens
  const perfect = row.passed === row.total;
  // Projects (design or code-quality) always warrant feedback; plain activities
  // only when not perfect.
  const isProject = a.feedback === "project" || a.feedback === "code";
  return isProject || !perfect;
}

// Walk a clone for student-authored source, skipping deps, build output, the
// overlaid canonical tests, and student.json (PII must not leave for the AI).
// Token-bounded: gather candidates, prioritise the student's own code under
// src/, then fill up to a byte cap - so when a submission is large the cap keeps
// the code that matters, not stray config.
function collectSourceFiles(clone, cap = 24000) {
  const skipDir = new Set(["node_modules", ".git", "dist", "build", "coverage", ".vite", "test", "tests", "__tests__"]);
  const keep = /\.(jsx?|tsx?|dart|css|scss|sass|html|py|md)$/i;     // code/markup + docs (README, HAUDEX.md, ...)
  const allowName = new Set(["package.json", "pubspec.yaml", "tailwind.config.js", "tailwind.config.cjs", "tailwind.config.ts"]);
  const skipName = /^(vite|vitest|eslint|prettier|babel|rollup)\b|\.config\.[cm]?[jt]s$/i; // build/lint boilerplate
  const cands = [];
  const walk = (d, rel) => {
    let entries = [];
    try { entries = readdirSync(d, { withFileTypes: true }); } catch { return; }
    for (const e of entries) {
      if (e.name.startsWith(".") || skipDir.has(e.name)) continue;
      const full = `${d}/${e.name}`;
      const r = rel ? `${rel}/${e.name}` : e.name;
      if (e.isDirectory()) { walk(full, r); continue; }
      if (e.name === "package-lock.json" || e.name === "student.json" || e.name === "RUBRIC.md") continue; // noise / PII / sent separately
      const named = allowName.has(e.name);
      if (!named && (!keep.test(e.name) || /\.(test|spec)\./i.test(e.name) || skipName.test(e.name))) continue;
      cands.push({ r, full });
    }
  };
  walk(clone, "");
  // student code first (src/ then shorter paths), so the cap keeps what matters
  cands.sort((a, b) => (a.r.startsWith("src/") ? 0 : 1) - (b.r.startsWith("src/") ? 0 : 1) || a.r.localeCompare(b.r));
  const out = [];
  let used = 0;
  for (const { r, full } of cands) {
    let body = "";
    try { body = readFileSync(full, "utf8"); } catch { continue; }
    if (body.length > 8000) body = body.slice(0, 8000) + "\n...[truncated]";
    if (used + body.length > cap) continue;
    used += body.length;
    out.push(`--- ${r} ---\n${body}`);
  }
  return out.join("\n\n");
}

// The grading rubric for this activity, used to ground design/quality feedback
// and the proposed score. Prefer the copy in the student's submission (clone);
// fall back to the canonical grader/<id>/RUBRIC.md (overlaid into every clone,
// so this resolves even for repos created before RUBRIC.md existed).
function collectRubric(clone, a) {
  for (const p of [`${clone}/RUBRIC.md`, `grader/${a.id}/RUBRIC.md`]) {
    try { const s = readFileSync(p, "utf8").trim(); if (s) return s; } catch { /* next */ }
  }
  return "";
}

// Images dominate token cost, so we send at most a couple. For responsive web
// projects the mobile + desktop shots cover both design and responsiveness;
// the tablet shot is redundant and skipped.
const MAX_IMAGES = 2;

// Reuse the screenshots a submission already has: a class may render them
// locally onto row.pages (pass previewDir), or reuse the Playwright shots a
// project CI publishes to a `previews` branch (`"previews": "branch"`).
function fetchBranchPreviews(clone) {
  try { quiet(`git -C ${clone} fetch -q origin previews --depth=1`); } catch { return []; }
  let listing = "";
  try { listing = sh(`git -C ${clone} ls-tree -r --name-only origin/previews`); } catch { return []; }
  const pngs = listing.split("\n").filter((f) => /^submission\/.+\.png$/i.test(f));
  if (!pngs.length) return [];
  const ts = (f) => f.split("/")[1];
  const latest = pngs.map(ts).sort().at(-1); // TS is ISO-ish, so lexical sort = chronological
  const inLatest = pngs.filter((p) => ts(p) === latest);
  // Prefer mobile + desktop; fall back to the first few if names differ.
  const preferred = inLatest.filter((f) => /mobile|desktop/i.test(f));
  const chosen = (preferred.length ? preferred : inLatest).sort().slice(0, MAX_IMAGES);
  const imgs = [];
  for (const f of chosen) {
    try {
      const buf = execSync(`git -C ${clone} show origin/previews:${f}`, { maxBuffer: 5e7 });
      imgs.push({ name: f.split("/").pop(), b64: buf.toString("base64") });
    } catch { /* skip this image */ }
  }
  return imgs;
}

function collectScreenshots(row, a, work, previewDir) {
  if (row.pages && row.pages.length && typeof previewDir === "function") {
    const imgs = [];
    for (const p of row.pages.slice(0, MAX_IMAGES)) {
      try { imgs.push({ name: p.rel, b64: readFileSync(`${previewDir(row)}/${p.img}`).toString("base64") }); } catch { /* skip */ }
    }
    if (imgs.length) return imgs;
  }
  if (a.previews === "branch") return fetchBranchPreviews(`${work}/${row.repo}`);
  return [];
}

// Did the code declare a real styling approach? Derived from the styling
// contract test: it passed iff it is NOT among the failing checks. null when
// the suite did not run (malformed) or the class has no such test.
function stylingDeclared(row) {
  if (!row.total) return null;
  return !(row.failures || []).some((f) => /styl/i.test(f.title));
}

// Generate the note for one (row, assignment). Returns
// { student, instructor, aiScore, aiAuthored }:
//  - student    what the learner sees (prose only, no scores, no AI mention)
//  - instructor the student prose + proposed grade + AI-authored likelihood + flags
//  - aiScore    the proposed TOTAL out of the activity's points, or null
//  - aiAuthored "low" | "medium" | "high" likelihood the work was AI-generated, or null
// Empty strings on any failure so grading is never blocked.
export async function aiNotes(row, a, { work = ".grade-work", previewDir } = {}) {
  const blank = { student: "", instructor: "", aiScore: null, aiAuthored: null };
  if (!MODELS_TOKEN) return blank;
  const clone = `${work}/${row.repo}`;
  const isCode = a.feedback === "code";              // code-quality project (back-end / non-frontend)
  const isProject = a.feedback === "project" || isCode; // both score a rubric and propose a grade
  const total = Number.isFinite(+a.totalPoints) ? +a.totalPoints : 100;
  const shots = collectScreenshots(row, a, work, previewDir);
  const declares = stylingDeclared(row);
  const rubric = collectRubric(clone, a);

  const failList = (row.failures || []).map((f) => `- ${f.title}`).join("\n");
  const stylingRule = shots.length
    ? "A screenshot of the running app is attached. If it looks like essentially unstyled default-browser HTML (Times New Roman, default margins, no layout or color), do NOT praise or critique the visual design - give code feedback only and say the design could not be assessed."
    : "No screenshot is attached; comment on code only.";

  // Everything after the ---INSTRUCTOR--- divider is instructor-only and never
  // shown to the student. The AI-authored-likelihood signal goes there for both
  // tiers; projects also get the proposed grade there.
  const vibe = "On its own final line add `AI-authored likelihood: low|medium|high - <one short reason>`: your private estimate of how likely this was generated by an AI assistant rather than written by a student at the level in the class context (signals: sophistication beyond the level, tutorial-style or over-explained comments, inconsistent or boilerplate patterns, polish that mismatches the failing checks). A soft signal for the instructor, NOT an accusation.";
  // The subjective half of a project's rubric is framed by flavor: design
  // (frontend, screenshot-led) or code quality (back-end / non-frontend).
  const studentLine = isCode
    ? "Then the STUDENT-FACING feedback: a short, encouraging paragraph (or a few bullets) on what works and a couple of concrete next steps in the code (structure, naming, error handling, edge cases). Do NOT mention scores, points, the rubric, AI authorship, or that this was AI-generated."
    : "Then the STUDENT-FACING feedback: a short, encouraging paragraph (or a few bullets) on what works and a couple of concrete next steps across code and, where the page is styled, design. Do NOT mention scores, points, the rubric, AI authorship, or that this was AI-generated.";
  const autoLine = isCode
    ? "(1) the AUTOMATED half - do NOT use the raw test pass-rate; translate the passed/failed checks into the rubric's weighted automated line items. Give each as `Item: x/max - reason`."
    : "(1) the AUTOMATED half - do NOT use the raw test pass-rate; translate the checks into the rubric's weighted line items (builds/runs, real styling approach, the behavior/structure contract, responsiveness), using the attached screenshot to judge whether it rendered and whether it is responsive at 375px. Give each line item as `Item: x/max - reason`.";
  const subjLine = isCode
    ? "(2) the CODE-QUALITY half - score each rubric criterion in that half (e.g. structure/architecture, naming, error handling, abstractions/OOP, organization, edge cases) from the code. Give each as `Criterion: x/max - reason`."
    : "(2) the DESIGN half - score each design criterion as `Criterion: x/max - reason` from the app, screenshots, and code.";
  const format = isProject
    ? [
        shots.length ? "Line 1 must be exactly `STYLING: rendered` or `STYLING: not-rendered`." : null,
        studentLine,
        "Then a line containing only `---INSTRUCTOR---` (everything after it is for the instructor only and is NEVER shown to the student).",
        `Then propose the full grade against the rubric, whose two halves sum to ${total}:`,
        autoLine,
        subjLine,
        `Then a line: \`Proposed total: N/${total}\` (automated + ${isCode ? "code quality" : "design"}).`,
        vibe,
      ].filter(Boolean).join(" ")
    : [
        "Write the STUDENT-FACING feedback: one short paragraph explaining why the failing checks below likely did not pass and which topics to review. Keep it brief. Do not mention AI authorship or that this was AI-generated.",
        "Then a line containing only `---INSTRUCTOR---` (everything after it is for the instructor only and is NEVER shown to the student).",
        vibe,
      ].join(" ");

  const userText = [
    classPrompt() && `Class context:\n${classPrompt()}`,
    rubric && `Grading rubric for this activity (ground your feedback and the proposed grade in this):\n${rubric}`,
    `Activity ${a.id}, worth ${total} points. Raw automated tests so far: ${row.score} - this is the raw test count, NOT the rubric's automated points; translate it per the rubric's weighted line items.`,
    isProject
      ? "This is a larger project graded against the rubric above."
      : "This is a short exercise.",
    stylingRule,
    failList ? `Automated checks that did not pass:\n${failList}` : "All automated checks passed.",
    `Student source:\n${collectSourceFiles(clone)}`,
    `Output format:\n${format}`,
  ].filter(Boolean).join("\n\n");

  const content = [{ type: "text", text: userText }];
  for (const img of shots) content.push({ type: "image_url", image_url: { url: `data:image/png;base64,${img.b64}` } });

  let text;
  try {
    const ask = (msgContent) => callModel({
      model: MODELS_NAME, temperature: 0.4, max_tokens: 800,
      messages: [{ role: "system", content: SYSTEM_PROMPT }, { role: "user", content: msgContent }],
    });
    let res = await ask(content);
    // Screenshots dominate the request body, so a large app plus its shots can
    // exceed the API's request-size limit (HTTP 413). Retry once code-only so
    // feedback is still produced - the design just cannot be judged from the
    // screenshot in that case.
    if (res.status === 413 && shots.length) {
      console.log(`  notes: ${row.repo} (${a.id}) oversized with screenshot(s); retrying code-only`);
      res = await ask(content.filter((c) => c.type === "text"));
    }
    if (!res.ok) { console.log(`  notes: ${row.repo} (${a.id}) skipped (HTTP ${res.status})`); return blank; }
    const j = await res.json();
    text = (j.choices?.[0]?.message?.content || "").trim();
  } catch (e) {
    console.log(`  notes: ${row.repo} (${a.id}) skipped (${e.message})`);
    return blank;
  }
  if (!text) return blank;

  // Peel off the STYLING marker - an instructor signal, never shown to students.
  let rendered = null;
  const sm = text.match(/^\s*STYLING:\s*(rendered|not-rendered)\b[^\n]*\n*/i);
  if (sm) { rendered = sm[1].toLowerCase() === "rendered"; text = text.slice(sm[0].length).trim(); }

  // Split the student-facing half from the instructor-only block (proposed
  // grade + AI-authored likelihood).
  const parts = text.split(/\n-{2,}\s*INSTRUCTOR\s*-{2,}\n/i);
  let student = parts[0].trim();
  const instrBlock = parts.length > 1 ? parts.slice(1).join("\n").trim() : "";
  // Defensive: never let an instructor-only line leak into the student copy if
  // the model forgot the divider.
  student = student.split("\n")
    .filter((ln) => !/^\s*(AI-authored likelihood|Proposed total|-{2,}\s*INSTRUCTOR)/i.test(ln))
    .join("\n").trim();

  const mt = instrBlock.match(/total[:\s]*([0-9]{1,3})\s*\/\s*[0-9]{1,3}/i);
  const aiScore = mt ? Math.min(total, +mt[1]) : null;
  const va = instrBlock.match(/likelihood[:\s]*(low|medium|high)/i);
  const aiAuthored = va ? va[1].toLowerCase() : null;

  let instructor = student;
  if (instrBlock) instructor += `\n\n---\n\n**For the instructor (not shown to the student):**\n\n${instrBlock}`;
  if (shots.length && rendered === false) {
    instructor += declares === true
      ? "\n\n> [!] Styling IS present in the code but the preview rendered unstyled - likely a CSS import/wiring issue. Judge the design from the code or a local run, not the screenshot."
      : "\n\n> [!] Preview rendered unstyled and no real styling approach was detected in the code.";
  }
  return { student, instructor, aiScore, aiAuthored };
}

// Generate notes with a small concurrency pool. A row is in scope when it
// warrants feedback AND it was either (re)graded this run OR has no note yet -
// the latter lets a later sweep fill in rows a previous run left empty (e.g.
// after a rate-limit), WITHOUT ever regenerating a note that already exists, so
// completed work is never re-billed. Mutates each handled row: r.notes,
// r.notesInstructor, r.aiScore.
export async function runNotesPass(rows, assignments, gradedThisRun, ctx = {}) {
  if (!MODELS_TOKEN) {
    if (assignments.some((a) => a["ai-grading"])) console.log("\n(ai feedback skipped: no MODELS_TOKEN in env)");
    return;
  }
  const byId = new Map(assignments.map((a) => [a.id, a]));
  const todo = rows.filter((r) => {
    if (!warrantsFeedback(r, byId.get(r.assignment))) return false;
    return gradedThisRun.has(`${r.repo}|${r.assignment}`) || !r.notes; // regraded, or still missing a note
  });
  if (!todo.length) return;
  console.log(`\nai feedback: generating notes for ${todo.length} submission(s) (concurrency ${POOL})...`);
  let i = 0;
  const worker = async () => {
    while (i < todo.length) {
      const r = todo[i++];
      const a = byId.get(r.assignment);
      const out = await aiNotes(r, a, ctx);
      r.notes = out.student;
      r.notesInstructor = out.instructor;
      r.aiScore = out.aiScore;
      r.aiAuthored = out.aiAuthored;
      if (r.notes) console.log(`  notes -> ${r.repo} (${r.assignment})${out.aiScore != null ? ` [proposed ${out.aiScore}/${a.totalPoints || 100}]` : ""}${out.aiAuthored ? ` [AI-likelihood ${out.aiAuthored}]` : ""}`);
    }
  };
  await Promise.all(Array.from({ length: Math.min(POOL, todo.length) }, worker));
}
